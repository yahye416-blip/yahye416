
// Addition Assignment
var y = 5;
y = y + 10;   //new y =  old y + 10
//  y+= 10 is a quicker way of typing  y = y + 10
console.log(y);      //output : 15

//Subtraction Assignment
var a=6;
a -= 2; 
console.log(a);    //output: 4

//Multiplication Assignment
let b = 6;
b *= 2; // same as b = b * 2
console.log(b); // 12

//Remainder Assignment
let r = 10;
r %= 3; // same as r = r % 3
console.log(r); // 1